//! 后端实现层
//!
//! 提供具体后端实现，包括单机、分布式等
//!
//! # Architecture
//!
//! ```text
//! ┌─────────────────────────────────────────────────────────────┐
//! │                    VGI Layer                                 │
//! │  ┌─────────────┐  ┌────────────┐                            │
//! │  │ VirtualGraph│  │ Backend    │                            │
//! │  │   trait     │  │  trait     │                            │
//! │  └─────────────┘  └────────────┘                            │
//! └─────────────────────────────────────────────────────────────┘
//!                              │
//!                              ▼
//! ┌─────────────────────────────────────────────────────────────┐
//! │              Backend Implementations                        │
//! │   ┌─────────────┐  ┌────────────┐  ┌──────────────┐        │
//! │   │ Single      │  │ Distributed│  │ External     │        │
//! │   │ Machine     │  │ Cluster    │  │ Database     │        │
//! │   │ Backend     │  │            │  │              │        │
//! │   └─────────────┘  └────────────┘  └──────────────┘        │
//! └─────────────────────────────────────────────────────────────┘
//! ```
//!
//! # Module Structure
//!
//! - `traits`: 重新导出 `vgi::traits` 中的 Backend trait
//! - `single_machine`: 单机后端实现

